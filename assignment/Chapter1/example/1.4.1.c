#include <stdio.h>

int main()
{

    int int_num;
    float float_num;

    printf("Enter a integer number: ");
    scanf("%d", &int_num);

    printf("Enter a float number: ");
    scanf("%f", &float_num);

    printf("%d Your integer number is: \n", int_num);

    printf("%f Your float number is: \n", float_num);

    return 0;
}
